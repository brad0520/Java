package boardApp;

public class Article {

	int num; 
	String title; 
	String body;
	String regDate;
	String nickname;
	int hit;

	public Article(int aNum, String aTitle, String aBody, String aRegDate, String aNickname, int aHit) {
		num = aNum; // 대입 연산		
		title = aTitle;
		body = aBody;
		regDate = aRegDate;
		nickname = aNickname;
		hit = aHit;
	}

}
