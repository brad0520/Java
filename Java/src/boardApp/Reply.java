package boardApp;

public class Reply {
	
	int num;
	int articleNum;
	String body; 
	String nickname;
	String regDate;

	public Reply(int num, int articleNum, String body, String nickname, String regDate) {
		this.num = num;
		this.articleNum = articleNum;
		this.body = body;
		this.nickname = nickname;
		this.regDate = regDate;
	}

}
